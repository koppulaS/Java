package fibnocc;

public class SwitchCase {

	public static void main(String[] args) {
		
		String name="kkkk";
		
		switch (name) {
		case "yyy":
			System.out.println("yyy");
		break;
		
	case "nnn":
		System.out.println("nnn");
		break;
		
	case "kkkk":
		System.out.println("kkkk");
		break;
		
	case "ppp":
		System.out.println("ppp");
		break;
		
		
		default:
			System.out.println("default stmt");
			break;
			
		}
	}
}

