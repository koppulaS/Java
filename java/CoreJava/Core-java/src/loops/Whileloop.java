package loops;

public class Whileloop {

	   public static void main(String[] args) {
		
		   int i=1;
		   
		   while(i<=10) {
			  System.out.println(i); 
			  i++;
		   }
		  
			 int[]arr= {2,7,3,4,90,78,73,8};
			 
			 for(int j=1;j<arr.length;j++) {
				System.out.println(arr[1]);
			 }
				for (int data:arr) {
					System.out.println(data);
				
			 
		   }
	}
}
