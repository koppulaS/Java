		   for(int j=10;  j>=0; j--) {
			  System.out.println(j); 
		   }
		   
		   int[] arr= {2,4,6,8,10,12};
		   for (int i=0;i< arr.length;i++) {
			   System.out.println(arr[i]);
			   
			
			}
